#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (FT_NULL);
	new->content = content;
	new->next = FT_NULL;
	return (new);
}
