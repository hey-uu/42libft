#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_lst;

	new_lst = malloc(sizeof(t_list));
	if (!new_lst)
		return (FT_NULL);
	new_lst->content = content;
	new_lst->next = FT_NULL;
	return (new_lst);
}
